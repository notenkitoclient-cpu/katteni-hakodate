async function generateArticle(writer, existing, today) {
  const client = new Anthropic({ apiKey: process.env.ANTHROPIC_API_KEY });

  const existingList = existing
    .map(a => `- [${a.category}]『${a.title}』(著: ${a.author})`)
    .join('\n');

  const month = parseInt(today.slice(5, 7));
  const season =
    month >= 3 && month <= 5  ? '春（桜・新生活・花見シーズン）' :
    month >= 6 && month <= 8  ? '夏（港まつり・海水浴・花火大会）' :
    month >= 9 && month <= 11 ? '秋（紅葉・収穫・函館グルメ最盛期）' :
                                '冬（雪・クリスマスファンタジー・流氷シーズン）';

  const systemPrompt = `あなたは「カッテニハコダテ」の専属ライター、${writer.name}です。

## あなたのプロフィール
${writer.profile}

## あなたの文体・口癖・クセ（これを厳守してください）
${writer.voice}
`;

  const userPrompt = `今日は ${today}（${season}）です。

## 既存記事（重複禁止）
${existingList || '（まだ記事はありません）'}

## 執筆依頼
上記と重複しない、函館に関する記事を1本、**あなた（${writer.name}）らしい文体・口癖・クセを必ず使って**執筆してください。

カテゴリ（以下から1つ選択）:
グルメ / 観光 / 暮らし / イベント / 特産品 / 人・ビジネス

⚠️ 重要な注意事項:
- あなたの「口癖」「書き出しパターン」「締めの型」を必ず守ること
- 観光パンフレット調は禁止。あなたの個性で書くこと
- 季節（${season}）に合ったテーマを優先

## 出力形式（JSONのみ。前後のテキスト不要）
{
  "slug": "半角英数字とハイフンのみ（例: hakodate-morning-market-summer）",
  "title": "記事タイトル（あなたらしいタイトルで、日本語）",
  "description": "記事の要約（80文字以内、日本語）",
  "category": "選択したカテゴリ名",
  "content": "記事本文（Markdown形式、あなたの文体で400〜600文字、##見出しを1〜2個使う。サクッと読める短さで、でもライターの個性は必ず出す）"
}`;

  const response = await client.messages.create({
    model: process.env.ARTICLE_MODEL || 'claude-sonnet-4-6',
    max_tokens: 2048,
    system: systemPrompt,
    messages: [{ role: 'user', content: userPrompt }],
  });

  const text = response.content[0].text.trim();
  const m = text.match(/```json\s*([\s\S]+?)\s*```/) || text.match(/(\{[\s\S]+\})/);
  if (!m) throw new Error(`JSONが見つかりませんでした:\n${text.slice(0, 300)}`);
  return JSON.parse(m[1]);
}

// ═══════════════════════════════════════════════════════════════
//  ファイル保存
// ═══════════════════════════════════════════════════════════════

function saveArticle(article, writer, today) {
  let slug = article.slug.replace(/[^a-z0-9-]/g, '-').replace(/-+/g, '-');
  if (fs.existsSync(path.join(ARTICLES_DIR, `${slug}.md`))) {
    slug = `${slug}-${today.replace(/-/g, '')}`;
  }

  const md = `---
title: "${article.title.replace(/"/g, '\\"')}"
description: "${article.description.replace(/"/g, '\\"')}"
category: "${article.category}"
date: "${today}"
author: "${writer.name}"
featured: false
---

${article.content.trim()}
`;

  const filePath = path.join(ARTICLES_DIR, `${slug}.md`);
  fs.writeFileSync(filePath, md, 'utf-8');
  return { filePath, slug };
}

function writeResult(slug, article, writer) {
  fs.writeFileSync('/tmp/article-result.json', JSON.stringify({
    slug,
    title:       article.title,
    category:    article.category,
    description: article.description,
    writer:      writer.name,
    writerId:    writer.id,
    writerEmoji: writer.emoji,
  }), 'utf-8');
}

